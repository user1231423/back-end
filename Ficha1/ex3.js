function mult(num1,num2){
    return console.log(num1 * num2);
}

function som(num1,num2){
    return console.log(num1 + num2);
}

function sub(num1,num2){
    return console.log(num1 - num2);
}

function div(num1,num2){
    return console.log(num1 / num2);
}

function elev(num1,num2){
    return console.log(Math.pow(num1,num2));
}
